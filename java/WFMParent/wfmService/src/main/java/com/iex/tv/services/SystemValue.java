package com.iex.tv.services;


public class SystemValue {
	private String name;
	private String value;
	
	public SystemValue(String name, String value) {
		super();
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
}
